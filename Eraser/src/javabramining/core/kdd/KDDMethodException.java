package javabramining.core.kdd;

public class KDDMethodException extends Exception
{
	public KDDMethodException(){super();}
	public KDDMethodException(String arg0, Throwable arg1){super(arg0, arg1);}
	public KDDMethodException(String arg0){super(arg0);}
	public KDDMethodException(Throwable arg0){super(arg0);}
}
